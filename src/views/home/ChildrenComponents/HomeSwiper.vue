<template>
  <swiper class="sw">
    <swiper-item v-for="item in banners" :key="item.id">
      <a :href="item.link">
        <img :src="item.image" alt=""  @load="imageLoad"/>
      </a>
    </swiper-item>
  </swiper>
</template>
<script>
import { Swiper, SwiperItem } from "@/components/common/swiper";
export default {
  name: "HomeSwiper",
  props: {
    banners: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      isLoad: false,
    };
  },
  methods: {
    imageLoad(){
      if(!this.isLoad){
        this.$emit('swiperLoad');
        this.isLoad = true
      }
    }
  },
  components: {
    Swiper,
    SwiperItem,
  },
};
</script>
